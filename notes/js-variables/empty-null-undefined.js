/*
  Empty:
  - Ao declarar uma variável, ela será empty (vazia) quando:
  
  Se Number {
    let variável = 0;
  }

  Se String {
    let variável = "";
  }

  Se Array {
    let array = [];
  }

  Se Objeto {
    let obj = {};
  }
*/

/*
  Null:
  - Quando você faz com que a variável seja nula, que ela não exista.
  - Null é um falsy value.

  Ex:
  let abacaxi = null;
  !abacaxi -> true
*/

/*
  Undefined:
  - Variável é declarada/usada mas não é atribuído nenhum valor à ela.

  -- OBS: Se tentar usar uma variável sem sequer declara-lá, o typeof dela também será undefined.

  Exemplos:
  nulo;
  let nulo;
*/